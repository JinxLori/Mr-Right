public class PersonaLetter {
    int imageId;
    String username;
    String date;
    String oneMessage;

    public PersonaLetter(int imageId, String username, String oneMessage, String date) {
        this.imageId = imageId;
        this.username = username;
        this.date = date;
        this.oneMessage = oneMessage;
    }

    public int getImageId() {
        return imageId;
    }

    public void setImageId(int imageId) {
        this.imageId = imageId;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getOneMessage() {
        return oneMessage;
    }

    public void setOneMessage(String oneMessage) {
        this.oneMessage = oneMessage;
    }
}
